-- Enable pgvector extension
CREATE EXTENSION IF NOT EXISTS vector;

-- Drop tables if they exist
DROP TABLE IF EXISTS checkout_velocity;
DROP TABLE IF EXISTS products;
DROP TABLE IF EXISTS calendar;

-- Create products table
CREATE TABLE products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    description TEXT,
    image_url TEXT,
    product_url TEXT,
    tags TEXT[] NOT NULL DEFAULT '{}',
    zip_codes TEXT[] NOT NULL DEFAULT '{}', -- Localized availability (empty means global)
    embedding vector(512) -- 512-dimensional vector for CLIP embeddings
);

-- Create calendar table
CREATE TABLE calendar (
    zip_code TEXT NOT NULL,
    date DATE NOT NULL,
    event_name TEXT NOT NULL,
    event_type TEXT NOT NULL DEFAULT 'festival', -- 'festival', 'pre_wedding', 'wedding_day', 'post_wedding'
    attire_tags TEXT[] NOT NULL DEFAULT '{}',
    is_festive BOOLEAN DEFAULT TRUE,
    PRIMARY KEY (zip_code, date)
);

-- Create Hybrid Recommendation PostgreSQL Function
CREATE OR REPLACE FUNCTION get_hybrid_recommendations(
    user_vibe vector(512),
    trending_tags TEXT[],
    is_cold_wave BOOLEAN,
    is_festive_season BOOLEAN,
    target_event_tags TEXT[],
    is_wedding_day BOOLEAN,
    target_zip_code TEXT
)
RETURNS TABLE (
    id BIGINT,
    name TEXT,
    description TEXT,
    image_url TEXT,
    product_url TEXT,
    tags TEXT[],
    vector_score FLOAT,
    tag_score FLOAT,
    final_score FLOAT
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        p.id, 
        p.name, 
        p.description,
        p.image_url,
        p.product_url,
        p.tags,
        
        -- Cosine similarity: 1 - cosine distance (<=>). Range: [0, 2] distance, so similarity is [ -1, 1 ].
        -- We clamp it to [0, 1] for normalization.
        COALESCE(1.0 - (p.embedding <=> user_vibe), 0.0)::FLOAT AS vector_score,
        
        -- Score based on tag intersections with trending YouTube tags AND active event tags
        (
            SELECT COALESCE(COUNT(*)::FLOAT, 0.0) 
            FROM unnest(p.tags) AS pt 
            WHERE pt = ANY(trending_tags) OR pt = ANY(target_event_tags)
        )::FLOAT AS tag_score,
        
        -- Overall hybrid score:
        -- 40% Visual Vibe similarity
        -- 30% Youtube Trending tag match / Event tag match
        -- 30% Climate/Festive/Wedding boosts
        (
            (COALESCE(1.0 - (p.embedding <=> user_vibe), 0.0) * 0.4) + 
            ((SELECT COALESCE(COUNT(*)::FLOAT, 0.0) FROM unnest(p.tags) AS pt WHERE pt = ANY(trending_tags) OR pt = ANY(target_event_tags)) * 0.3) +
            
            -- Climate boost
            (CASE WHEN is_cold_wave AND 'winter' = ANY(p.tags) THEN 0.15 ELSE 0.0 END) +
            
            -- Festive boost
            (CASE WHEN is_festive_season AND 'festive' = ANY(p.tags) THEN 0.15 ELSE 0.0 END) +
            
            -- Event/Wedding boost
            (CASE WHEN is_wedding_day AND 'ceremonial' = ANY(p.tags) THEN 0.3 ELSE 0.0 END) +
            (CASE WHEN array_length(target_event_tags, 1) > 0 AND p.tags && target_event_tags THEN 0.15 ELSE 0.0 END)
        )::FLOAT AS final_score
    FROM products p
    WHERE (p.zip_codes = '{}' OR target_zip_code = ANY(p.zip_codes))
    ORDER BY final_score DESC;
END;
$$ LANGUAGE plpgsql;


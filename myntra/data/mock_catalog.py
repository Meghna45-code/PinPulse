def generate_vector(seed_text, dim=64):
    """Generate a deterministic pseudo-vector from a seed string."""
    np.random.seed(hash(seed_text) % (2**32))
    vec = np.random.randn(dim)
    vec = vec / np.linalg.norm(vec)  # Normalize
    return vec.tolist()


